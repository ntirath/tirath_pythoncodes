a = float(input("Enter the value to be verified = "))
if (a > 0):
    print("The value entered a =",a,"is positive")
elif (a < 0):
    print("The value entered a =",a,"is negative")
elif (a == 0):
    print("The value entered a =",a,"is equal to zero")